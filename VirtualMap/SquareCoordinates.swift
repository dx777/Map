import Foundation

class SquareCoordinates {
    private(set) var x1: Int
    private(set) var x2: Int
    private(set) var x3: Int
    private(set) var x4: Int
    private(set) var y1: Int
    private(set) var y2: Int
    private(set) var y3: Int
    private(set) var y4: Int
    
    init(x1: Int, x2: Int, x3: Int, x4: Int, y1: Int, y2: Int, y3: Int, y4: Int) {
        self.x1 = x1
        self.x2 = x2
        self.x3 = x3
        self.x4 = x4
        self.y1 = y1
        self.y2 = y2
        self.y3 = y3
        self.y4 = y4
    }
}
